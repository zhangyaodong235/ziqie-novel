老郑说——从那天起我的腿就成了天气预报。要下雨了腿就疼，比中央电视台还准。
到了安定村，老郑的第一件事不是去卫生所看腿——他去看了地形。项目部的选址在一个黄土梁子上，周围好几条深沟。老郑拄着一根棍子爬上去看了半天，下来的时候腿疼得额头上全是汗珠。但是他说了一句话——这个位置好，视野开阔，适合建控制点。
他到项目部那天晚上，我让人给他安排了一间一楼的宿舍——不用爬楼梯。他谢了我一声，把全站仪从车上搬下来，仔细地擦了一遍。全站仪上有一个凹痕——那是当年滚石留下的。他每次都拿手指摸摸那个凹痕，像摸一个老朋友。
老张是坐绿皮火车来的。慢车，从四川南充出发，在宝鸡转车，到兰州再转，到定西再转。一路上换了三趟车走了两天。到安定村的时候天已经黑了。他背着一口炒菜锅——那是他自己从老家带的——和一把磨得雪亮的菜刀。
老张叫张继修，五十八岁，四川南充人。他是项目部的炊事员，工资在整个项目部里是最低的。但是他不在乎——他说干活嘛，钱多钱少，够花就行。
他以前在广东的工厂食堂干过三年。工厂食堂一天要做四餐——早餐、中餐、晚餐、夜宵。一个人管几百号人的伙食，从早上四点半忙到晚上十点半。他说广东的夏天热，厨房里没有空调，只有一台排风扇。排风扇嗡嗡嗡地转着，锅里的油滋啦啦地溅着，汗水顺着脊背往下流，把围裙都湿透了。
后来他回四川老家开了一个小饭馆。开了两年，生意不好，关了。他老婆说他不是当老板的料——太老实，熟人来吃饭他不收钱。老婆跟他离婚了。儿子跟着老婆过，一年见两次。
老张说——我这人没本事。就一件——做饭。做了大半辈子饭，没做出什么名堂。但是工地上的年轻人吃了我的饭都说香。这就够了。
他到项目部头三天是最难的。厨房还没建好，只有一间临时搭的棚子，里面一口大铁锅，一个煤气灶。菜是买不到的——最近的集市在二十里外。厨房里只有一袋面粉和几个萝卜。老张头三天给大家做的是清水煮面——白水烧开，面下进去，煮熟了捞出来，拌上咸菜末。咸菜末是他从老家带来的，用塑料瓶装着，一小瓶，省着吃。
工人们端着碗呼噜呼噜地吃着，没人抱怨。老张站在灶台后面看着，心里不是滋味。他想做点好的给大家吃，但是什么都没有。第三天晚上，他一个人蹲在厨房棚子外面，抽了半包烟。
第四天，他去镇上跑了一趟，用三轮车拉回来米、油、盐、酱油、醋、干辣椒、花椒。中午他做了一顿酸辣面。四川的酸辣面——面条煮好以后过凉水，拌上醋、酱油、辣椒油、花椒面、蒜末、葱花。简简单单一碗面，但是味道对了——酸得开胃，辣得出汗。工地上的工人们端着碗，吃了一碗又一碗。川籍的工人说——张师傅，这面正宗。老张嘿嘿笑了两声，又去忙活了。
后来厨房建好了，老张有了正经的灶台。他开始变着花样给大家做饭。四川的回锅肉、麻婆豆腐、水煮鱼——鱼是从定西城里买来的冷冻草鱼——还有西北的拉条子、刀削面。每天三餐，一周不重样。
老张有个习惯——吃饭的时候他从来不坐，总是站在打饭的窗口后面，看每个人舀菜。谁的碗里菜少了，他提着勺子过来添一勺。谁忙着干活没来吃，他把饭菜放在锅里热着——不管多晚，人回来总有热饭。
有一次陈栋半夜两点从洞里回来，浑身泥浆，累得话都不想说。走进食堂，老张还在等。看见陈栋进来，老张二话没说，走到灶台前把火打开。五分钟不到，一碗热气腾腾的鸡蛋面端到陈栋面前。面上卧着一个荷包蛋，汤里飘着葱花。
陈栋说——张师傅，你早点睡。以后不用等我。
老张说——你吃你的。我等是我的事。
项目启动会是在进场后的第五天开的。会议室不大，墙上的白灰已经起皮了，桌椅是临时搬进来的折叠桌椅，坐下去吱吱嘎嘎地响。人到齐了——三十一个人。陈栋挨着墙坐在角落，老周坐在门边，老郑把伤腿伸直了搁在桌子下面，老张没来——他在厨房烧水。
人到齐了，会议室里有短暂的沉默。我坐在前面，看着底下的这些人。来自天南地北的三十一个人。年纪最大的老郑五十六岁，年纪最小的是龚建平——那年二十四岁，刚毕业的研究生，带着一副黑框眼镜，坐在最后一排，手里拿着一本笔记本。
三十一个人。每个人背后都有一个家。有父母，有配偶，有孩子。他们都把家留在了远方，把自己搬到了这片黄土塬上。为的是一件事——把水引过来。
我站起来，打开投影仪。幕布上出现一张照片——安定村干涸的河床，泥块裂开，裂缝宽得能伸进去一只手。
我说——这是离我们项目部不到一公里的地方。这条河干了三十多年了。
翻到下一张——冉继萍凌晨三点在沟底等泉水。手电筒的光照在水坑上，水坑里只有浅浅一层水。
下一张——驼背的老太太扶着墙走路。氟骨症。水喝的。
下一张——画河的那面墙。蓝色的波浪线歪歪扭扭，粉笔写着两个字：大河。
会议室里安静了。有人在挪凳子——可能是坐着不舒服。有人咳嗽了一声。然后又是安静。
我关掉投影仪。日光灯嗡嗡地响着。我说——咱们来这儿，不是来赚钱的。赚钱去别的地方也能赚。咱们来这儿，是来还债的。还一笔欠了六十多年的债。
底下没人说话。陈栋低着头看着桌面。老周的手搁在膝盖上，手指微微弯曲，像在握着什么。老郑的左腿微微抖了一下——可能是疼了。
然后我说——开工。
会议散了。人一个一个走出去。会议室里只剩下我一个人，和满屋子的烟味。我收拾桌子上的资料，发现龚建平的笔记本忘在座位上了。我翻开看了一眼——第一页写着一行字。
让水过去。
字不大，一笔一划，写得很用力。墨水的颜色是蓝的。
我合上笔记本，拿在手里。准备明天还给他。
那天晚上我在院子里站了很久。黄土高原的夜晚安静得让人发慌。没有城市的车声，没有人声，连狗叫声都很少。只有风声——风从塬上刮过来，吹着院子里的旗杆，旗杆上的红旗哗啦啦地响。
陈栋走了出来。他递给我一根烟。我接过来点上。两个人站在院子里，抽着烟，看着远处黑黢黢的山梁。
陈栋说——明天进洞。
我说——嗯。
他说——这个洞不好打。地质太复杂。古滑塌面、涌水、沉陷——什么情况都可能有。
我说——你是工程部长，你说了算。
他深吸一口烟，烟头在黑暗里亮了一下，又暗下去。
他说——在地底下待久了，人会有一种感觉——觉得自己也被埋了。土在头顶上，厚厚的黄土，比什么都沉。你上不去，下不来，就卡在那儿。有一种绝望是慢慢渗出来的。
我没说话。我等他继续。
他说——但是你不能停。停了就真的出不来了。只能往前——一镐头一镐头，一炮一炮，一米一米。你往前一点，洞就深一点。洞深一点，水就近一点。
他把烟头扔在地上，用脚踩灭了。转身往回走。走了几步又停下来，回头说——明天我第一个进洞。
陈栋的身影消失在走廊尽头。我在院子里又站了一会儿。
三十一个人。明天就要开始打洞了。从一个黄土梁子下面打进去，穿过古滑塌面，穿过涌水层，穿过未知的地质，一路往前。不知道前面会遇到什么，但是必须往前走。
我想起程杰的那把镢头。铁锈斑斑的木柄上嵌着1958年的黄土。那时候他们也是用镢头挖土的。六十三年前的镢头和今天的掘进机，干的其实是一件事——把水引过来。
只是那时候没干成。现在要干成了。
天上有流星划过。很快，一眨眼就没了。但是在那一瞬间很亮。我许了一个愿——工程顺利，不要出人命。
后来这个愿没有保住。龚建平还是牺牲了。那是几个月以后的事。第六章再说。
第二天早上六点。天还没亮透，东边的山梁上有一道淡金色的光。项目部的人到齐了，在院子里站成一排。没有仪式——没有鞭炮，没有剪彩，没有领导讲话。只有三十一个人。陈栋站在最前面，穿着一身工装，脚上是一双劳保鞋。
他回头看了我们一眼。然后转身，往隧洞口走去。
他身后跟着老周、老郑、龚建平、还有别的技术员和工人们。我走在最后面。走了几步，我回头看了一眼院子——老张站在厨房门口，围着白围裙，手里拿着勺子，目送我们离开。
隧洞口在项目部往西一公里的山脚下。洞口不大，直径大概六米，黑乎乎的，像一个张开的嘴。地上已经铺了施工便道，挖掘机停在旁边，掘进机的钢头在晨光里泛着光。
陈栋在洞口站了一会儿。他从兜里掏出手电筒，打开，往里照了照。光柱在黑暗里切出一条通道，照在裸露的黄土洞壁上。洞壁是湿的——沁着水珠。不是流出来的水，是渗出来的，像洞在出汗。
他说——进。
第一个人进去了。第二个。第三个。掘进机发动了，轰鸣声在洞里回荡放大，震得洞壁上的土块簌簌往下掉。
我也走进去了。洞里的空气是凉的——比外面低好几度。有一种泥土和潮气混合的味道。脚下是泥浆，踩下去噗嗤噗嗤的。手电筒照在前面的人身上，影子和洞壁叠在一起，晃来晃去的。
这就是引洮隧洞的第一天。六十三年的等待，从这一刻开始变成了一米一米的前进。
洞外面，安定村才刚刚醒来。画河的小男孩背着书包往村小走，经过那面矮墙的时候，他停下来，拿出粉笔，在蓝色的河下面又加了一条鱼。第三条鱼了。他画完看了看，满意了，又继续往村小走。
冉继萍又去沟底了。这是她的习惯——在太阳出来之前去看一眼泉眼。今天泉眼的水比昨天多了一点。可能是昨天夜里下了小雨，地下水渗了一些进来。她蹲下来，用水瓢舀了一瓢水，尝了一口。还是咸的。她把水倒在桶里，等着第二瓢渗出来。
程杰坐在院子里的枣树底下。阳光照在他脸上，皱纹像干河床的裂缝。他闭着眼睛，嘴唇微微动着——没有人能听见他在说什么。也许是在跟兄弟说话。也许是在和水说话。
李炳章的竹板又响了。他今天来了一段新的——
三十一个人站在洞口，
望着黑窟窿往前走。
里面不是金银宝，
里面是一条河的路。
他的嗓子哑了。但是竹板打得还是那么有力。
那天晚上我在日记里写到——三月二十一日，晴。项目部三十一人，今日正式进洞。洞内沁水，地质复杂。但路已经开始了。
